class MathsOperations:
    @classmethod
    def addition(cls, a, b):
        """
        Fonction d'addition
        :param a:
        :param b:
        :return: a+b
        """
        return a + b

    @classmethod
    def soustraction(cls, a, b):
        """
        Fonction de soustraction
        :param a:
        :param b:
        :return: a-b
        """
        return a - b

    @classmethod
    def multiplication(cls, a, b):
        """
        Fonction de mutiplication
        :param a:
        :param b:
        :return: a*b
        """
        return a*b

    @classmethod
    def division_entiere(cls, a, b):
        """
        Fonction de division entiere
        :param a:
        :param b:
        :return: a//b
        """
        return a//b